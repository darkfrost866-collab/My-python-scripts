"""gmail_manager.py — Gmail label management + IMAP job storage via App Password"""
import imaplib
import smtplib
import email
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from email.header import decode_header
import re
from datetime import datetime

try:
    from config import EMAIL_USER, EMAIL_PASS
except Exception:
    EMAIL_USER = ""
    EMAIL_PASS = ""

LABEL_JOBS        = "Job-AI/All-Jobs"
LABEL_APPLIED     = "Job-AI/Applied"
LABEL_SHORTLISTED = "Job-AI/Shortlisted"
LABEL_GENERATED   = "Job-AI/Generated-Resumes"


# ── IMAP helpers ──────────────────────────────────────────────────────────────

def _connect_imap():
    imap = imaplib.IMAP4_SSL("imap.gmail.com")
    imap.login(EMAIL_USER, EMAIL_PASS)
    return imap


def _label_exists(imap, label):
    status, mailboxes = imap.list()
    if status != "OK":
        return False
    for mb in mailboxes:
        if mb and label.lower() in mb.decode(errors="ignore").lower():
            return True
    return False


def create_labels():
    """Create all Job-AI Gmail labels (folders) if they don't exist yet."""
    if not EMAIL_USER or not EMAIL_PASS:
        print("  Gmail credentials not configured — skipping label creation.")
        return False
    try:
        imap = _connect_imap()
        for label in [LABEL_JOBS, LABEL_APPLIED, LABEL_SHORTLISTED, LABEL_GENERATED]:
            if not _label_exists(imap, label):
                imap.create(f'"{label}"')
                print(f"  Created Gmail label: {label}")
            else:
                print(f"  Label exists: {label}")
        imap.logout()
        return True
    except Exception as exc:
        print(f"  Gmail label setup failed: {exc}")
        return False


# ── SMTP helpers ──────────────────────────────────────────────────────────────

def _send_to_label(subject: str, html_body: str, label: str):
    """Send an email to yourself that Gmail will auto-label via a filter,
    and also manually move it via IMAP append."""
    msg = MIMEMultipart("alternative")
    msg["From"]    = EMAIL_USER
    msg["To"]      = EMAIL_USER
    msg["Subject"] = f"[{label}] {subject}"
    msg["X-Job-AI-Label"] = label
    msg.attach(MIMEText(html_body, "html"))

    with smtplib.SMTP("smtp.gmail.com", 587) as srv:
        srv.starttls()
        srv.login(EMAIL_USER, EMAIL_PASS)
        srv.send_message(msg)


