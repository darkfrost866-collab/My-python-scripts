def list_gmail_jobs(label: str = LABEL_JOBS, max_results: int = 10):
    """List subject lines of emails in a Job-AI Gmail label."""
    if not EMAIL_USER or not EMAIL_PASS:
        print("  Gmail credentials not configured.")
        return []
    try:
        imap = _connect_imap()
        imap.select(f'"{label}"')
        status, data = imap.search(None, "ALL")
        if status != "OK" or not data[0]:
            imap.logout()
            return []
        ids = data[0].split()[-max_results:]
        results = []
        for msg_id in reversed(ids):
            status2, msg_data = imap.fetch(msg_id, "(RFC822.SIZE BODY[HEADER.FIELDS (SUBJECT DATE)])")
            if status2 == "OK":
                raw_header = msg_data[0][1].decode(errors="ignore")
                subj_match = re.search(r"Subject: (.+)", raw_header)
                date_match = re.search(r"Date: (.+)", raw_header)
                results.append({
                    "subject": subj_match.group(1).strip() if subj_match else "No subject",
                    "date": date_match.group(1).strip() if date_match else "",
                })
        imap.logout()
        return results
    except Exception as exc:
        print(f"  IMAP error: {exc}")
        return []


